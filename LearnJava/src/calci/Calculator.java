package calci;

public class Calculator {
	public void add() {
		int num1 = 10;
		int num2 = 2;
		int sum = num1 + num2;
		System.out.println("sum of numbers is:" + sum);
	}

	public void minus() {
		int num1 = 40;
		int num2 = 20;
		int diff = num1 - num2;
		System.out.println("difference of numbers is:" + diff);
	}

	public void multiply() {
		int num1 = 7;
		int num2 = 6;
		int multi = num1 * num2;
		System.out.println("multiply result is:" + multi);
	}

	public void divide() {
		int num1 = 36;
		int num2 = 6;
		int div = num1 / num2;
		System.out.println("division result is:" + div);
	}

}
